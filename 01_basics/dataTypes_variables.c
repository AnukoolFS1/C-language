#include <stdio.h>

int main() {
    int age = 25;
    float height = 3.9;
    char Grade = 'A';
    double PI = 3523.14159265358979323846;

    printf("Age %d\n", age);
    printf("Height %.15f\n", height);
    printf("Grade %c\n", Grade);
    printf("Double precision %.20f\n", PI);

    return 0;
}